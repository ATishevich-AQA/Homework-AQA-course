public class StringOperations {
    public String getSum(String line1, String line2) {
        return line1 + line2;
    }

    public static String getReversSum(String line1, String line2, String line3) {
        return line3 + line2 + line1;
    }

    public int getLength(String line) {
        return line.length();
    }

    public int getSqaredLength(String line) {
        return line.length();
    }
    public int getCubeLength(String line) {
        return line.length();
    }


}
